// Find voice from language
export function addSpeak(lang) {
    let voice;
    let langName = filterVoicenames(lang);
    for (let i = 0; i < voices.length; i++) {
        if (voices[i].startsWith(langName)) {
            voice = voices[i];
            break;
        }
    }
    return voice;
}
function filterVoicenames(lang) {
    let result = lang.replace("German", "Deutsch");
    return result;
}